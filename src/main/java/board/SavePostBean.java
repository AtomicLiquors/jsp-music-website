package board;

public class SavePostBean {

	private int num;
	private String id;
	private int savePost;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getSavePost() {
		return savePost;
	}
	public void setSavePost(int savePost) {
		this.savePost = savePost;
	}
	
	
	
}
